# very basic use:
if True:
    print("Action A was taken")
if False:
    print("Action B was taken")

a = True and False

# notice the colon ':' character
if a: # if a == True
    print("a is True")
if not a: # if a == False
    print("a is False")

# everything after the 'if' must become a Boolean variable
# the == operator returns a Boolean value
if (128 % 2 == 0):
    print("128 is an even number")



